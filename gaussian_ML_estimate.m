function [ m_hat, s_hat ] = gaussian_ML_estimate( X )  
%{  
    ?????  
        ?????????????  
  
    ?????  
        X?????  
  
    ?????  
        m_hat??????????????????????  
        s_hat??????????????????????  
%}  
  
% ????  
[~, N] = size(X);  
% ????????????????????????????????  
m_hat = (1/N) * sum(transpose(X))';  
  
% ?????????????????????N?????????  
s_hat = zeros(1);  
for k = 1:N  
    s_hat = s_hat + (X(:, k)-m_hat) * (X(:, k)-m_hat)';  
end  
s_hat = (1/N)*s_hat;  
end  
  
